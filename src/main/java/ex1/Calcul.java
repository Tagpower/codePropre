package ex1;

/**
 * Classe permettant de faire des calculs
 * @author ETY0004
 *
 */
public class Calcul {

	/**
	 * Méthode additionnant deux entiers
	 * @param a
	 * @param b
	 * @return la somme de a et b
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/**
	 * Méthode soustrayant deux entiers
	 * @param a
	 * @param b
	 * @return la différence de a et b
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
